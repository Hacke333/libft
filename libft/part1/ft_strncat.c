#include "../libft.h"

char	*ft_strncat(char *dest, const char *src, unsigned int n)
{
	int				len;
	unsigned int	i;

	len = ft_strlen(dest);
	i = 0;
	while (i < n && src[i])
	{
		dest[len + i] = src[i];
		i++;
	}
	dest[len + i] = '\0';
	return (dest);
}
