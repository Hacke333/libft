#include "../libft.h"

int	ft_strnequ(char const *s1, char const *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	n--;
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < n)
		i++;
	if ((s1[i] - s2[i]) == 0)
		return (1);
	return (0);
}
